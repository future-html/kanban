@app.route('/signup', methods=['POST'])
def signup():
    """Register a new user and save them to MongoDB."""
    data = request.get_json()

    # 1. Validate that all required fields are present
    if not data or not data.get('username') or not data.get('email') or not data.get('password'):
        return jsonify({'error': 'Missing required fields (username, email, password)'}), 400

    # 2. Check if a user with this email already exists
    if user_collection.find_one({'email': data['email']}):
        return jsonify({'error': 'User with this email already exists'}), 409

    # 3. Create the user object (using plain text password for mock project)
    new_user = {
        'username': data['username'],
        'email': data['email'],
        'password': data['password']  # <--- Plain string stored here
    }

    # 4. Insert into database
    result = user_collection.insert_one(new_user)
    
    # 5. Prepare the userInfo object to send back to the frontend (excluding password)
    user_info = {
        'id': str(result.inserted_id),
        'username': new_user['username'],
        'email': new_user['email']
    }

    return jsonify({"message": "User created successfully", "userInfo": user_info}), 201


@app.route('/login', methods=['POST'])
def login():
    """Authenticate a user and return their info to the frontend."""
    data = request.get_json()
    # print(data, 'data received from frontend')  # Debugging statement
    # print(data.get('email'))
    
    # 1. Validate request payload
    if not data or not data.get('email') or not data.get('password'):
        return jsonify({'error': 'Missing email or password'}), 400

    # 2. Find the user by their email in MongoDB
    user = user_collection.find_one({'email': data['email']})

    # 3. Validate user exists AND the plain text passwords match
    if user and user['password'] == data['password']:  # <--- Plain string comparison here
        
        # 4. Prepare the userInfo object to send to the frontend
        # Even in a mock project, it's good practice not to send the password back!
        user_info = {
            'id': str(user['_id']),
            'username': user['username'],
            'email': user['email']
        }
        
        return jsonify({
            "message": "Login successful", 
            "userInfo": user_info
        }), 200
    else:
        return jsonify({"error": "Invalid email or password"}), 401




# 3. POST /board: Add a new board
# dashboard id is generated by MongoDB when we create a new document in the todolist collection.

@app.route('/board', methods=['POST'])
def create_board():
    data = request.get_json()
    user_id = data.get('userId')
    board_name = data.get('boardName')
    dashboard_id = data.get('dashboardId') # <-- New: Optional target dashboard

    if not user_id or not board_name:
        return jsonify({"error": "userId and boardName are required"}), 400

    try:
        user_oid = ObjectId(user_id) # Defining user_oid properly
        
        new_board = {
            "_id": ObjectId(),
            "boardName": board_name,
            "columns": []
        }
        
        if dashboard_id:
            # SCENARIO A: Targeting a specific dashboard (Member or Owner)
            dashboard_oid = ObjectId(dashboard_id)
            
            # Query: Must match the exact dashboard ID AND the user must be the owner OR a member
            query = {
                "_id": dashboard_oid,
                "$or": [
                    {"userId": user_oid},
                    {"member": user_oid}
                ]
            }
            # We set upsert=False here. If the shared dashboard doesn't exist, we shouldn't create a fake one.
            allow_upsert = False 
            
        else:
            # SCENARIO B: No dashboardId provided. Assume user's personal default dashboard.
            # Query: Just look for the user's own document
            query = {"userId": user_oid}
            # We set upsert=True here. If the user doesn't have a personal document yet, create one.
            allow_upsert = True

        result = todolist_collection.update_one(
            query,
            {
                "$push": {"todos": new_board},
                "$setOnInsert": {"member": []} 
            },
            upsert=allow_upsert
        )
        
        # If we targeted a specific dashboard but nothing matched, it means bad ID or no permissions
        if dashboard_id and result.matched_count == 0:
            return jsonify({"error": "Dashboard not found or you lack permission to add boards here"}), 403

        return jsonify({
            "message": "Board created successfully", 
            "boardId": str(new_board["_id"])
        }), 201

    except Exception as e:
        # Good practice to print the actual error to your console for debugging
        print(f"Error creating board: {e}") 
        return jsonify({"error": "Invalid ID format or database error"}), 400
    

