@app.route('/board/task/move', methods=['PUT'])
def move_task():
    data = request.get_json()
    dashboard_id = data.get('dashboardId')
    task_id = data.get('taskId')
    old_board_id = data.get('oldBoardId')
    old_column_id = data.get('oldColumnId')
    new_board_id = data.get('newBoardId')
    new_column_id = data.get('newColumnId')
    new_index = data.get('newIndex') # We DO need this!

    if new_index is None: # Explicitly check None because index could be 0
        return jsonify({"error": "Missing required fields"}), 400

    try:
        dash_oid = ObjectId(dashboard_id)
        task_oid = ObjectId(task_id)

       # STEP 1: READ the task data using an Aggregation Pipeline
        dash_oid = ObjectId(dashboard_id)
        task_oid = ObjectId(task_id)

        pipeline = [
            # 1. Find the specific dashboard
            {"$match": {"_id": dash_oid}},
            
            # 2. Deconstruct the 'todos' (boards) array
            {"$unwind": "$todos"},
            # Match the specific board
            {"$match": {"todos._id": ObjectId(old_board_id)}},
            
            # 3. Deconstruct the 'columns' array
            {"$unwind": "$todos.columns"},
            # Match the specific column
            {"$match": {"todos.columns._id": ObjectId(old_column_id)}},
            
            # 4. Deconstruct the 'tasks' array
            {"$unwind": "$todos.columns.tasks"},
            # Match the exact task we want to move
            {"$match": {"todos.columns.tasks._id": task_oid}},
            
            # 5. Make the matched task the root object to return
            {"$replaceRoot": {"newRoot": "$todos.columns.tasks"}}
        ]

        result = list(todolist_collection.aggregate(pipeline))
        
        # If the list isn't empty, we found our task data
        task_data = result[0] if result else None

        if not task_data:
            return jsonify({"error": "Original task not found"}), 404

        # STEP 2: PULL (Remove) the task from its old location
        todolist_collection.update_one(
            {"_id": dash_oid},
            {
                "$pull": {"todos.$[board].columns.$[col].tasks": {"_id": task_oid}}
            },
            array_filters=[
                {"board._id": ObjectId(old_board_id)},
                {"col._id": ObjectId(old_column_id)}
            ]
        )

        # STEP 3: PUSH (Insert) the task into the new location at the specific index
        todolist_collection.update_one(
            {"_id": dash_oid}, # does not require check existence because it is move task
            {
                "$push": {
                    "todos.$[board].columns.$[col].tasks": {
                        "$each": [task_data],       # The data we copied in Step 1
                        "$position": int(new_index) # The exact slot to drop it into!
                    }
                }
            },
            array_filters=[
                {"board._id": ObjectId(new_board_id)},
                {"col._id": ObjectId(new_column_id)}
            ]
        )

        return jsonify({"message": "Task moved successfully"}), 200

    except Exception as e:
        return jsonify({"error": "Invalid data format or server error"}), 400
    

    
